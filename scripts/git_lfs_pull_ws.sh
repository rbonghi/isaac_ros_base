bold=`tput bold`
red=`tput setaf 1`
green=`tput setaf 2`
yellow=`tput setaf 3`
reset=`tput sgr0`


main()
{
    local local_folder=$1

    if [[ -z $local_folder ]] ; then
        echo "${red}Please write a folder you want git lfs pull${reset}"
        exit 33
    fi

    # Build a list of folder available
    for d in $local_folder/*/
    do
        local folder="${d%/}"
        echo "git-lfs: $folder"
        cd $folder
        git lfs pull
    done
}

main $@
